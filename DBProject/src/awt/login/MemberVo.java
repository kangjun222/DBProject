package awt.login;
public class MemberVo {
	private String id;
	private String password;
	private String id2;
	private String Pwd;
	
	
	
	

	   public MemberVo() {

	   }

	   public MemberVo(String id, String password) {
	     this.id=id;
	     this.password=password;
	   }

	public String getId() {
		return id;
	}



	public String getPassword() {
		return password;
	}
	
	public String getId2() {
		return id2;
	}


	public String getPwd() {
		return Pwd;
	}
}
	